    private void backButtonActionPerformed(final java.awt.event.ActionEvent evt) {
        updateData();
        new ImportJobsWindow(data).setVisible(true);
        dispose();
}
    /**
     * 
     * @param evt
     */
    private void nextButtonActionPerformed(final java.awt.event.ActionEvent evt) {
        updateData();
        try {
            new CSVMapImportJobsWindow(data).setVisible(true);
            dispose();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "I/O Exception when accessing source file", "File I/O Error", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(CSVImportJobsWindow.class.getName()).log(Level.INFO, "File-Based I/O Exception", ex);
        } catch (CSVException ex) {
            JOptionPane.showMessageDialog(this, "CSV Exception occured when accessing source file", "CSV Parser Error", JOptionPane.ERROR_MESSAGE);            
            Logger.getLogger(CSVImportJobsWindow.class.getName()).log(Level.INFO, "CSV Exception", ex);
        }
}
